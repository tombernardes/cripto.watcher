package main

import (
	"io"
	"os"
	"time"

	"github.com/go-echarts/go-echarts/v2/charts"
	"github.com/go-echarts/go-echarts/v2/components"
	"github.com/go-echarts/go-echarts/v2/opts"
	"github.com/tombernardes/cripto.watcher/crosscutting"
	"github.com/tombernardes/cripto.watcher/domain"
)

var candleCharts []domain.Chart

func init() {
	go updateChart()
}

func updateChart() {
	candleCharts = crosscutting.Charts
	for {
		candleCharts = crosscutting.Charts
		time.Sleep(10 * time.Millisecond)
	}
}

func getTimeSeries() []klineData {
	ret := []klineData{}
	for _, v := range candleCharts[0].Points {
		date := time.Unix(0, v.OpenTime*1000000).Format("2006-01-02T15:04:05.999")
		ret = append(ret, klineData{date: date, data: [4]float64{v.ClosePrice, v.OpenPrice, v.MinPrice, v.MaxPrice}})
	}
	return ret
}

type klineData struct {
	date string
	data [4]float64
}

var kd []klineData

func main() {
	time.Sleep(5 * time.Second)
	println("Loaded")
	for {
		f, err := os.Create("kline.html")
		if err != nil {
			panic(err)

		}
		page := components.NewPage()
		page.SetLayout(components.PageFlexLayout)
		page.AddCharts(
			klineBase(),
			/*klineDataZoomInside(),
			klineDataZoomBoth(),
			klineDataZoomYAxis(),
			klineStyle(),*/
		)

		page.Render(io.MultiWriter(f))
		time.Sleep(800 * time.Millisecond)
	}
}

func klineBase() *charts.Kline {
	kd = getTimeSeries()
	kline := charts.NewKLine()

	x := make([]string, 0)
	y := make([]opts.KlineData, 0)
	for i := 0; i < len(kd); i++ {
		x = append(x, kd[i].date)
		y = append(y, opts.KlineData{Value: kd[i].data})
	}

	kline.SetGlobalOptions(
		charts.WithTitleOpts(opts.Title{
			Title: "BTCUSDT",
		}),
		charts.WithXAxisOpts(opts.XAxis{
			SplitNumber: 20,
		}),
		charts.WithYAxisOpts(opts.YAxis{
			Scale: true,
		}),
		charts.WithDataZoomOpts(opts.DataZoom{
			Start:      95,
			End:        100,
			XAxisIndex: []int{0},
		}),
	)

	kline.SetXAxis(x).AddSeries("kline", y)
	return kline
}

